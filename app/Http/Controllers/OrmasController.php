    {
        $b=Ormas::create($request->except(['bidang']));


        $bidang = $request->bidang; 
        foreach($bidang as $a){
            $b->bidang()->create(
                [
                    'bidang' => $a
                ]
            );
            }

        return view('admin.ormas.index');
